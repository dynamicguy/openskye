package org.openskye.domain;

/**
 * The different types of attributes allowed.
 */
public enum AttributeType {
    /**
     * A text/string attribute
     */
    TEXT,
    /**
     * A numeric attribute.
     */
    NUMERIC,
    /**
     * A date attribute
      */
    DATE,
    /**
     * An enumerated attribute
     */
    ENUMERATED
}
